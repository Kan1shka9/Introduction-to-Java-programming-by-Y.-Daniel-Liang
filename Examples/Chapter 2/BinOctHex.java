package Chapter_2;

public class BinOctHex {

	public static void main(String[] args) {
		// Denote binary integer literal
		System.out.println(0B1111); // Displays 15
		
		// Denote octal integer literal
		System.out.println(07777); // Displays 4095
		
		// Denote hexadecimal integer literal
		System.out.println(0XFFFF); // Displays 65535
	}

}
